CREATE TABLE hiragana (
    hiragana_charID int NOT NULL AUTO_INCREMENT,
    hiragana_char VARCHAR(255),
    romaji VARCHAR(255)
    PRIMARY KEY (hiragana_charID));

CREATE TABLE katakana (
    katakana_charID int NOT NULL AUTO_INCREMENT,
    katakana_char VARCHAR(255),
    romaji VARCHAR(255),
    PRIMARY KEY (katakana_charID));

CREATE TABLE kanji (
    kanji_charID int NOT NULL AUTO_INCREMENT,
    kanji_char VARCHAR(255),
    hiragana_ver VARCHAR(255),
    PRIMARY KEY (kanji_charID));

